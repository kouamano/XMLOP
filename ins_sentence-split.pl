#!/usr/bin/perl

while(<>){
	$_ =~ s/ORG>\. /ORG>\. <;\/>/g;
	$_ =~ s/RES>\. /RES>\. <;\/>/g;
	$_ =~ s/W>\. /W>\. <;\/>/g;
	$_ =~ s/<\/sec><sec/<\/sec><;\/> <sec/g;
	$_ =~ s/<\/p><p>/<\/p><;\/> <p>/g;
	$_ =~ s/([a-z0-9A-Z]{5,}\.\s)/$1<;\/> /g;
	$_ =~ s/([a-z0-9\/\<][a-zA-Z0-9\/\<][a-zA-Z0-9\/][a-zA-Z0-9\>]\.\s)/$1<;\/> /g;
	$_ =~ s/([a-z0-9\/\<][a-zA-Z0-9\/\<][a-zA-Z0-9\/][a-zA-Z0-9\>][\)\]]\.\s)/$1<;\/> /g;
	$_ =~ s/([A-Z][0-9A-Z][0-9A-Z][0-9A-Z]\.\s)/$1<;\/> /g;
	$_ =~ s/([A-Z][0-9A-Z][0-9A-Z][0-9A-Z][\)\]]\.\s)/$1<;\/> /g;
	print $_;
}
